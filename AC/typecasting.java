package AC;

public class typecasting {
    public static void main(String[] args) {
        float a = 25.23f;
        int b = (int) a;
        System.out.println(b);

        char one = 'a';
        int number = one;
        System.out.println(number);
        char two = 'A';
        int number2 = two;
        System.out.println(number2);

    }
}
